public class Author {
    private int authorId;
    private String authorName;
    private String authorPwd;
    private String authorQq;
    private String authorWeixin;
    private String authorMail;
    private String authorPhone;
    private String authorDescription;

    public int getAuthorId() {
        return authorId;
    }

    public void setAuthorId(int authorId) {
        this.authorId = authorId;
    }

    public String getAuthorName() {
        return authorName;
    }

    public void setAuthorName(String authorName) {
        this.authorName = authorName;
    }

    public String getAuthorPwd() {
        return authorPwd;
    }

    public void setAuthorPwd(String authorPwd) {
        this.authorPwd = authorPwd;
    }

    public String getAuthorQq() {
        return authorQq;
    }

    public void setAuthorQq(String authorQq) {
        this.authorQq = authorQq;
    }

    public String getAuthorWeixin() {
        return authorWeixin;
    }

    public void setAuthorWeixin(String authorWeixin) {
        this.authorWeixin = authorWeixin;
    }

    public String getAuthorMail() {
        return authorMail;
    }

    public void setAuthorMail(String authorMail) {
        this.authorMail = authorMail;
    }

    public String getAuthorPhone() {
        return authorPhone;
    }

    public void setAuthorPhone(String authorPhone) {
        this.authorPhone = authorPhone;
    }

    public String getAuthorDescription() {
        return authorDescription;
    }

    public void setAuthorDescription(String authorDescription) {
        this.authorDescription = authorDescription;
    }
}
